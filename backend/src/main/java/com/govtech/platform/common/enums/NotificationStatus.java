package com.govtech.platform.common.enums;

/**
 * Read state of a notification delivered to a citizen.
 *
 * <ul>
 *   <li>UNREAD – notification has not been acknowledged by the citizen.</li>
 *   <li>READ   – citizen has read or marked the notification as read.</li>
 * </ul>
 */
public enum NotificationStatus {
    UNREAD,
    READ
}
